NUMBER=
{
        /**
        *        Transforma una medida de radianes a grados
        *
        *        @param {int}
        *        @return {int}
         *        @version        1.0        -  12-01-2008
        */
        get_grados_to_rad:function(deg)
        {
          return deg * (Math.PI / 180);
        }
}

